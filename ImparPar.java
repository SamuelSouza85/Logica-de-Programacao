public class ImparPar {
    public static void main(String[] args) {
        int numero = 999;
        int resto = numero % 2;
        if(resto == 0){
            System.out.println("O número " + numero + " é par");
        }
        else{
            System.out.println("O número " + numero + " é ímpar");
        }

    }
}
