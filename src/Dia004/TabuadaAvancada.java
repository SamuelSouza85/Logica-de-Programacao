package Dia004;

public class TabuadaAvancada {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Dia003.Tabuada do numero: " + 1);
            for (int j = 0; j <= 10; j++) {
                System.out.println(i + "x" + j + " = " + (i*j));
            }
        }
    }
}