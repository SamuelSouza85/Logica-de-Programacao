public class Arrays {
    public static void main(String[] args) {
        double[] notas = new double[4];
        notas [0] =  5.5;
        notas [1] =  8;
        notas [3] =  10;
        notas [2] =  7.5;
        for (int i = 0; i < notas.length; i++) {
            System.out.println(notas[i]);
        }
    }
}
